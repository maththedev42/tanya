export type ValidationIssue = {
  id: string;
  severity: "error" | "warning";
  message: string;
  files?: string[];
  // When true, an unrepaired error flips the run VERDICT to FAIL (not just the
  // repair loop / footer). Reserved for objective, zero-false-positive static
  // checks (goose annotations, migration collision, localization parity) —
  // heuristic validators keep driving repair without gating the verdict.
  gating?: boolean;
};

export type ValidationSummary = {
  passed: boolean;
  issues: ValidationIssue[];
  firedValidatorIds?: string[];
  primaryPlatform?: "ios" | "macos" | "android" | "backend" | "landing" | "web" | "script" | "unknown";
};

export type ValidationManifest = {
  changedFiles: string[];
  artifactsRead?: string[];
  artifactsCreated?: string[];
  contextFilesRead?: string[];
  verification?: string[];
  blockers?: string[];
  git?: {
    root: string | null;
    head: string | null;
  };
  // HEAD at run start — baseline for diff-based reachability checks (deleted
  // analytics). Absent when the run had no starting commit.
  sessionBaseHead?: string;
};

